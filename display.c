//Initiliases the display
void displayInitialise() {
	pin_low(CS);
	pin_low(RST);
	usleep(500000);
	pin_high(RST);

	sendCommand(0xA3); //Set Bias to 1/7
	sendCommand(0xA0); //Normal Orientation (Vertical)
	sendCommand(0xC0); //Normal Orientation (Horizontal)
	sendCommand(0x40); //Set cursor to (0,0)
	sendCommand(0x2C); //Voltage
	sendCommand(0x2D); //Voltage
	sendCommand(0x2F); //Voltage
	sendCommand(0x27); //Resistor Ratio

	sendCommand(0xAF); //Turn Display On
	sendCommand(0xA4); //Normal Display Mode
	sendCommand(0x81); //Dynamic Contrast Mode
	sendCommand(22); //Set Contrast

	sendCommand(0xAC);

	clearDisplay();
}

//Sends data to the display
void sendData(uint8_t data) {
	pin_high(A0); //ao high
	for (int i = 7; i >= 0; i--) {
		if ((data & (1 << i)))
			pin_high(SID); //SID high
		else
			pin_low(SID); //SID low
		pin_high(SCLK); //SCLK high
		pin_low(SCLK); //SCLK low
	}
}

//Sends a command to the display
void sendCommand(uint8_t command) {
	pin_low(A0); //ao low
	for (int i = 7; i >= 0; i--) {
		if ((command & (1 << i)))
			pin_high(SID); //SID high
		else
			pin_low(SID); //SID low
		pin_high(SCLK); //SCLK high
		pin_low(SCLK); //SCLK low
	}
}

//Updates display with the displayBuffer
void updateDisplay() {
	uint8_t p, col, maxcol = 128;
	for (p = 0; p < 8; p++) {
		sendCommand(0xB0 | pagemap[p]); //Set Page
		sendCommand(0x01);
		sendCommand(0x10);

		for (col = 0; col < maxcol; col++) {
			sendData(displayBuffer[(128 * p) + col]);
		}
	}
}

//Sets pixel at (X,Y) On(1) or Off(0)
void setPixel(uint8_t x, uint8_t y, uint8_t state) {
	if (y > 63)
		y = 63;
	else if (y < 0)
		y = 0;
	if (state == 1)
		displayBuffer[x + (y / 8) * 128] |= 1 << (7 - (y % 8));
	else
		displayBuffer[x + (y / 8) * 128] &= ~(1 << (7 - (y % 8)));
}

//Draws a line from (x1,y1) to (x2,y2)
void drawLine(float x1, float y1, float x2, float y2) {
	if (y2 > y1) {
		for (int p = y1; p < y2; p++)
			setPixel(x2, p, 1);
	} else if (y2 < y1) {
		for (int p = y1; p > y2; p--)
			setPixel(x2, p, 1);
	} else
		setPixel(x2, y2, 1);
}

//Draws or removes a cross at (x,y)
void drawCross(uint8_t x, uint8_t y, uint8_t state) {
	setPixel(x - 2, y - 2, state);
	setPixel(x - 1, y - 1, state);
	setPixel(x, y, state);
	setPixel(x + 1, y + 1, state);
	setPixel(x + 2, y + 2, state);
	setPixel(x - 1, y + 1, state);
	setPixel(x - 2, y + 2, state);
	setPixel(x + 1, y - 1, state);
	setPixel(x + 2, y - 2, state);

}
